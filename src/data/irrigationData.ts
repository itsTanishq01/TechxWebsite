// Utility function to calculate water requirement
export const calculateWaterRequirement = (
  cropData: CropIrrigationData | undefined,
  growthStage: string,
  fieldArea: number
): { min: number; max: number; totalMin: number; totalMax: number } | null => {
  if (!cropData) return null;
  
  let waterRange: string = "";
  
  switch (growthStage) {
    case "seedingStage":
      waterRange = cropData.seedingStage;
      break;
    case "vegetativeStage":
      waterRange = cropData.vegetativeStage;
      break;
    case "floweringFruitingStage":
      waterRange = cropData.floweringFruitingStage;
      break;
    case "maturityStage":
      waterRange = cropData.maturityStage;
      break;
    default:
      return null;
  }
  
  // Parse the range (format: "X-Y mm")
  const [min, max] = waterRange.replace(" mm", "").split("-").map(Number);
  
  // Water requirement in cubic meters (1 mm water depth over 1 hectare = 10 cubic meters)
  // Convert field area from hectares to square meters (1 hectare = 10,000 sq meters)
  const areaInHectares = fieldArea;
  const minWaterRequired = (min * areaInHectares * 10);
  const maxWaterRequired = (max * areaInHectares * 10);
  
  return {
    min: min,
    max: max,
    totalMin: minWaterRequired,
    totalMax: maxWaterRequired
  };
};
